class User:
    def __init__(self, first_name, last_name, age, occupation, identification_number, profile_password):
        self.user_first_name = first_name
        self.user_last_name = last_name
        self.user_age = age
        self.user_occupation = occupation
        self.user_identification_number = identification_number
        self.user_profile_password = profile_password



